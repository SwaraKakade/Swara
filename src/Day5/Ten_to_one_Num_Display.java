public class Ten_to_one_Num_Display

{
	public void number() 
	{
        int i=10;
        while(i>0)
        {   
        	System.out.println(i);
        	i--;
        	
        }

	}
	
		public static void main(String args[])
		{
			
			Ten_to_one_Num_Display o =new Ten_to_one_Num_Display();
					
             o.number();
		}
}
		
		
		



