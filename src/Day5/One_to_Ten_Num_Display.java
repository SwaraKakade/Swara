package Day5;

public class One_to_Ten_Num_Display 
{
	public void number() 
	{
        int i=1;
        while(i<=10)
        {   
        	System.out.println(i);
        	i=i+1;
        	
        }

	}
	
		public static void main(String args[])
		{
			
			One_to_Ten_Num_Display o =new One_to_Ten_Num_Display();
					
             o.number();
		}
}
